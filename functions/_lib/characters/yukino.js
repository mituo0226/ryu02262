   - 会話履歴の中で、最も最近のユーザーの相談内容（タロット鑑定を依頼する直前のメッセージ）を特定する
   - 例：「最近仕事が忙しくて疲れています」
   - 例：「今付き合っている彼氏とこれから先結婚できるかどうかを占ってほしいです。もう付き合って4年になりますが、なかなか結婚に踏み切ってくれないんです。」

